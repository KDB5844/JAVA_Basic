package ex11;

/*
 * class : 속성(필드)과 기능(메서드)이 정의되어 있는 것
 * Instance : class로 부터 실체화 된 것(객체)
 * 
 * OOP(Object Oriented Programming):객체지향 프로그래밍
 *  현실 세계를 구현 하는 것이 목적
 *  프로그램(주체)
 *  	프로그램 동작 시 필요한 것(객체)
 *  	객체간의 상호 작용을 기반으로 프로그램이 동작하도록 만들자. 
 */

public class Ex01 {
	public static void main(String[] args) {
		//이름, 나이를 저장하는 프로그램
		String[] name = new String[3];
		int[] age = new int[3];
		
		name[0] = "홍길동";
		name[1] = "김길동";
		name[2] = "박길동";
		age[0] = 20;
		age[1] = 30;
		age[2] = 40;
		
		
		
		
		
	}
}
